fun Modifier.shimmerBackground(shape: Shape): Modifier = composed {
    val shimmerColors = listOf(
        // VocÃª pode ajustar as cores para combinar com o tema do seu app
        MaterialTheme.colorScheme.surfaceVariant.copy(alpha = 0.6f),
        MaterialTheme.colorScheme.surfaceVariant.copy(alpha = 0.2f),
        MaterialTheme.colorScheme.surfaceVariant.copy(alpha = 0.6f),
    )

    val transition = rememberInfiniteTransition(label = "ShimmerTransition")
    val translateAnimation = transition.animateFloat(
        initialValue = 0f,
        targetValue = 1000f, // Um valor alto para garantir que o gradiente cruze a tela
        animationSpec = infiniteRepeatable(
            animation = tween(durationMillis = 1200), // Ajuste a velocidade aqui
            repeatMode = RepeatMode.Restart
        ),
        label = "ShimmerTranslateAnimation"
    )

    this.background(
        brush = Brush.linearGradient(
            colors = shimmerColors,
            start = Offset.Zero,
            end = Offset(x = translateAnimation.value, y = translateAnimation.value)
        ),
        shape = shape
    )
}

